package serviceManager.vo;

public class ServiceManagerVo {
	
	private Integer id; 
	
	private Integer api_id;
    
    private String userName;
    
    private String api_Name;

    private String api_Name_Cn;
    

	public Integer getApi_id() {
		return api_id;
	}

	public void setApi_id(Integer api_id) {
		this.api_id = api_id;
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getApi_Name() {
		return api_Name;
	}

	public void setApi_Name(String api_Name) {
		this.api_Name = api_Name;
	}

	public String getApi_Name_Cn() {
		return api_Name_Cn;
	}

	public void setApi_Name_Cn(String api_Name_Cn) {
		this.api_Name_Cn = api_Name_Cn;
	}

    
    
}
