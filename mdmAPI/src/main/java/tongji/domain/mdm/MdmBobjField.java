package tongji.domain.mdm;

public class MdmBobjField {
	
	private String id;
	private String tableId;
	private String bobjType;
	private String field;
	private String fieldCn;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTableId() {
		return tableId;
	}
	public void setTableId(String tableId) {
		this.tableId = tableId;
	}
	public String getBobjType() {
		return bobjType;
	}
	public void setBobjType(String bobjType) {
		this.bobjType = bobjType;
	}
	public String getField() {
		return field;
	}
	public void setField(String field) {
		this.field = field;
	}
	public String getFieldCn() {
		return fieldCn;
	}
	public void setFieldCn(String fieldCn) {
		this.fieldCn = fieldCn;
	}

	
	

}
